type wineSample struct {
	features []float32 // alcohol, volatile acidity, citric acid, pH (normalized)
	target   float32   // quality / 10
}

func loadWineDataset() ([]wineSample, error) {
	lines := strings.Split(strings.TrimSpace(wineQualityRedCSV), "\n")
	var all []wineSample
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if line == "" {
			continue
		}
		parts := strings.Split(line, ";")
		if len(parts) < 12 {
			return nil, fmt.Errorf("bad wine row: %q", line)
		}
		volatile, err := strconv.ParseFloat(parts[1], 32)
		if err != nil {
			return nil, err
		}
		citric, err := strconv.ParseFloat(parts[2], 32)
		if err != nil {
			return nil, err
		}
		pH, err := strconv.ParseFloat(parts[8], 32)
		if err != nil {
			return nil, err
		}
		alcohol, err := strconv.ParseFloat(parts[10], 32)
		if err != nil {
			return nil, err
		}
		quality, err := strconv.ParseFloat(parts[11], 32)
		if err != nil {
			return nil, err
		}
		feat := []float32{
			norm(alcohol, 8, 15),
			norm(volatile, 0.1, 1.6),
			float32(citric), // already 0–1
			norm(pH, 2.7, 4.0),
		}
		all = append(all, wineSample{
			features: feat,
			target:   float32(quality / 10.0),
		})
	}
	if len(all) < 20 {
		return nil, fmt.Errorf("wine dataset too small (%d rows)", len(all))
	}
	return all, nil
}

func norm(x, lo, hi float64) float32 {
	if hi <= lo {
		return float32(x)
	}
	v := (x - lo) / (hi - lo)
	if v < 0 {
		v = 0
	}
	if v > 1 {
		v = 1
	}
	return float32(v)
}

func splitDataset(all []wineSample, trainFrac float64) (train, val []wineSample) {
	n := len(all)
	trainN := int(float64(n) * trainFrac)
	if trainN < 1 {
		trainN = 1
	}
	if trainN >= n {
		trainN = n - 1
	}
	return all[:trainN], all[trainN:]
}

func samplesToBatches(samples []wineSample) (inputs [][]float32, targets [][]float32) {
	inputs = make([][]float32, len(samples))
	targets = make([][]float32, len(samples))
	for i, s := range samples {
		inputs[i] = append([]float32(nil), s.features...)
		targets[i] = []float32{s.target}
	}
	return inputs, targets
}
